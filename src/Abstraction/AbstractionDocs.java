package Abstraction;

public class AbstractionDocs {
   /*
    * =====================Abstraction===================
    * #Process of hiding implementation of details in the 
    *   program is known as abstraction.
    *   
    *  #To achieve abstraction :
    *    a> Abstract Class:
    *     #Class which contains abstract(Unimplemented) and non abstract
    *      methods(implemented)is known as abstract class.
    *        *abstract method:
    *            ->Method which does not have implementation/body
    *            e.g.
    *              abstract void sun();
    *              abstract void print(int a, String b);
    *     #We cannot create object of abstract class.
    *     #Every abstract class must have at least one child
    *     class and child class must override abstract methods.
    *     
    *              syntax:
    *                 abstract class class_name{
    *                 //abstract methods
    *                 e.g.
    *                   abstract void read();
    *                 //non abstract methods
    *                     void sum (){
    *                     }
    *                     }
    *    b> Interface:
    *    #Same as class which contains public abstract methods and 
    *    public static final variables only.
    *    #We can't create object of interface.
    *    #Every interface must have implementation class and must override
    *    abstract methods.
    *    #Its a 100 % abstraction
    *    
    *        Syntax:
    *          interface interface_name{
    *          //Public abstract methods
    *          
    *          //public static final variables
    *          }
    *      
    */
}
