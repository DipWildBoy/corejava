package Inheritance;

public class InheritanceDocs {
	/*
	 * ===================================Inheritance===============================================
	 * #Code re-usability.
	 * #Process of accessing properties and methods of parent/super/base class to child/sub/derive 
	 * class is known as Inheritance.
	 * #Also known as (IS-A) relationship.
	 * #Its a Generalization to Specialization process.
	 * #We cannot inherit private properties.
	 * #We cannot inherit constructor.
	 * #We can inherit static variable and method as well.
	 * #We can inherit only one class at a time (i.e. only one parent class at a time)
	 * 
	 * syntax:
	 *    class child_class extends parent_class_name{
	 *    //properties=>parent properties+child properties
	 *    
	 *    // methods  =>parent methods+child methods
	 *    }
	 *    e.g.
	 *      Class A{
	 *      }
	 *      Class B extends A{
	 *      }
	 */

}
